export const i18n = {
    'pt-br': pt,
    en,
    es,
};

export function useTranslations(overrideLang?: Language) {
  const storedLanguage = useUserStore((state) => state.user?.language);
  // Prioridade: 1. idioma sobrescrito, 2. idioma do usuário, 3. padrão
  const lang: Language = overrideLang || storedLanguage || 'pt-br';
  return i18n[lang];
}
